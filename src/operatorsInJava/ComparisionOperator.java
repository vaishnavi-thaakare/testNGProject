package operatorsInJava;

public class ComparisionOperator {
	public static void main(String[] args) {
		//Comparison Operator  - >,<,<=,>=,!=
		int physics=40;
		int chemistry=40;
		int mathematics=40;
		int ram=45;
		if (ram>=physics) {
			System.out.println(" The ram is passed in 'Physics'");
		} if (ram>=chemistry) {
			System.out.println(" The ram is passed in 'Chemistry'");
		}
		if (ram>=mathematics) {
			System.out.println(" The ram is passed in 'Mathematics'");
		}
		
	}

}
