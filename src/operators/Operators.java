package operators;

public class Operators {
	public static void main (String[] args) {
		/* Types of operator - 1] Arithmetic operators- add, sub, multi, div, modules
		                       2] Assignment operators
		                       3] Logical operators
		                       4] Comparison operator
		                       */
		
		int a=12;
		int b=45;
		int add=a+b;
		int sub=a-b;
		int multi=a*b;
		int div = a/b;
		int mod= a%b;
		System.out.println(" The addition of two number "+ a + "and "+ b +" is "+ add);
		System.out.println(" The substraction of "+ a +" and "+ b +"is "+ sub);
		System.out.println(" The multiplication of "+ a +" and "+ b +" is "+ multi);
		System.out.println(" The division of "+ a +" and "+ b +" is "+ div);
		System.out.println(" The remainder after dividing "+ a + " by "+ b + mod);
		

	}

}
